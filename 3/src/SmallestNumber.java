import java.util.Scanner;

public class SmallestNumber 
{

	public static void main(String[] args) 
	{
		
		int a=5;
		int b=20;
	
		
		if(a==b)
		{
			System.out.println("Both are equal");
			
		}
		
		else if(a<b)
		{	
			
			System.out.println("Smallest Value is..."+a);
		}
		else
		{
			System.out.println("Smallest Value is..."+b);
		}
	}

}
